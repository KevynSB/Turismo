        Respecto al origen del lago de Atitlán, hay dos opiniones. Una de ellas opina que el lago es un viejo cráter muerto del Volcán de los Chocoyos  y la otra que el surgimiento de los volcanes interrumpió el curso de los tres ríos que vienen del norte, los cuales, al reunir sus aguas en el lugar, dieron origen al lago. <br> <br>
        Además, se dice que donde está el lago hubo una isla que fue el epicentro de la actividad maya. Está rodeado por más de 10 pueblos mayas: Santa Catarina Palopó, San Antonio Palopó, San Lucas Tolimán, Santiago Atitlán, San Pedro La Laguna, San Juan La Laguna, San Pablo La Laguna, San Marcos La Laguna, Santa Cruz La Laguna, San Francisco Panajachel o Panajachel, Santa Clara La Laguna y otros, a estos pueblos se puede viajar desde lanchas que están a la orilla del lago, y rodeado por 3 volcanes: Tolimán, Atitlán y San Pedro.
