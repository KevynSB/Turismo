                <p>Cuenta con un mirador de más de 50 metros de altura para observar los 350 metros de largo de las pozas naturales del complejo natural. Las cataratas de hasta 40 pies de alto y todas las pozas que son alimentadas por riachuelos, maravillan al visitante.
